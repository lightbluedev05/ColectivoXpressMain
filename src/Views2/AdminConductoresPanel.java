    private void listar_conductores(){
        DefaultTableModel modelo = (DefaultTableModel)tabla_conductores.getModel();
        modelo.setRowCount(0);
        
        List<Conductor> conductores = admin.ver_conductores();
        for(Conductor conductor:conductores){
            modelo.addRow(new Object[]{conductor.get_dni(), conductor.get_correo(), conductor.get_departamento()});
        }
        
        tabla_conductores.setModel(modelo);
    }
    
    private void ver_datos_conductor(){
        DefaultTableModel modelo = (DefaultTableModel)tabla_datos_conductor.getModel();
        modelo.setRowCount(0);
        
        String dni = dni_buscar_input.getText();
        Conductor conductor = admin.buscar_conductor(dni);
        
        if(conductor==null){
            modelo.addRow(new Object[]{"ERROR", "DNI NO EXISTE"});
        } else {
            modelo.addRow(new Object[]{"DNI", conductor.get_dni()});
            modelo.addRow(new Object[]{"Nombres", conductor.get_nombre()});
            modelo.addRow(new Object[]{"Correo", conductor.get_correo()});
            modelo.addRow(new Object[]{"Fecha de Nacimiento", conductor.get_fecha_nacimiento()});
            modelo.addRow(new Object[]{"Distrito", conductor.get_distrito()});
            modelo.addRow(new Object[]{"Provincia", conductor.get_provincia()});
            modelo.addRow(new Object[]{"Departamento", conductor.get_departamento()});
            modelo.addRow(new Object[]{"Dias de Descanso", conductor.get_dias_descanso()});
            modelo.addRow(new Object[]{"Contrase√±a", conductor.get_contrasena()});
        }
        
        tabla_datos_conductor.setModel(modelo);
    }
    
    private void eliminar_conductor(){
        String dni = dni_eliminar_input.getText();
        boolean exito = admin.eliminar_conductor(dni);
        if(!exito){
            resultado_eliminar.setText("No se pudo borrar");
            return;
        }
        resultado_eliminar.setText("Conductor eliminado!!");
    }
    
